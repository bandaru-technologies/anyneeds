export const CITIES = Object.keys(CITY_STATE_MAP).sort();

export function lookupState(cityInput) {
  if (!cityInput) return '';
  const normalized = cityInput.trim().toLowerCase();
  const match = Object.keys(CITY_STATE_MAP).find(
    (c) => c.toLowerCase() === normalized
  );
  return match ? CITY_STATE_MAP[match] : '';
}
